// Inheritance using Constructor Functions

const Person = function (firstName, birthYear) {
  this.firstName = firstName;
  this.birthYear = birthYear;
};

Person.prototype.calcAge = function () {
  console.log(2037 - this.birthYear);
};

const Student = function (firstName, birthYear, course) {
  // we use the call method here
  Person.call(this, firstName, birthYear);
  this.course = course;
};

// We cannot do like this
// Here what we are specifying is the Student Prototype and the Person Prototype should be exactly same object. Which is wrong
Student.prototype = Person.prototype;

// Linking prototypes
Student.prototype = Object.create(Person.prototype);

Student.prototype.introduce = function () {
  console.log(`My name is ${this.firstName} and I study ${this.course}`);
};

const mike = new Student("Mike", 2020, "Computer Science");
mike.introduce();
mike.calcAge();

console.log(mike.__proto__); // Person object
console.log(mike.__proto__.__proto__); // Person constructor

console.log(mike instanceof Student); //true
console.log(mike instanceof Person); //true,
console.log(mike instanceof Object); //true

// Manipulationg Prototype Chain:

// If we dont do this then Student.prototype.constructor will point to Person Constructor
Student.prototype.constructor = Student; // now it will point to Strudent constructor
